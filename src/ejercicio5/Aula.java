package ejercicio5;

public class Aula {
	// Atributos
	private int idNum;
	private int numMaxEstudiantes;
	private String materia;
	
	// Constructores
	
	public Aula() {
		super();
		this.idNum=0;
		this.numMaxEstudiantes=0;
		this.materia = "";
	}

	
	public Aula(int idNum, int numMaxEstudiantes, String materia) {
		super();
		this.idNum = idNum;
		this.numMaxEstudiantes = numMaxEstudiantes;
		this.materia = materia;
	}
	
	
	
	
	
}
